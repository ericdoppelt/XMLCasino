package UI.Selectors;

/**
 * Very basic enumerated type that only specifies three types of input the user can give.
 * Each enumerated type is matched to a Key in the language bundles, which dictate the text that is shown on the Ready Button.
 * @author Eric Doppelt
 */
public enum SelectorType {
    ACTION,
    WAGER,
    NEWGAME
}
